// Arithmetic Operators - + | - | * | / | % |

#include <iostream>
using namespace std;

int main() {
        int a = 10 , b = 3;

        cout << a + b << endl;  // Addition
        cout << a - b << endl;  // Subtraction
        cout << a * b << endl;  // Multiply
        cout << a / b << endl;  // Division
        cout << a % b << endl;  // Module - remainder after division

        return 0;
}